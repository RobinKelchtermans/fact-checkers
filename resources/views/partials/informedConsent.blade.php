<h5>
    Doel van de studie
</h5>
<p>
    Het doel van de studie is om na te gaan of het personaliseren van gamificationtechnieken een positieve invloed kan hebben op <b>fact checking</b>. Hierbij wordt gebruik gemaakt van een online platform dat u dagelijks uw nieuws zal lezen en aan fact checking doen. 
    <br>
    <br>
    We onderzoeken ook of het anonimiseren van de nieuwsbronnen een effect zal hebben op het fact checken van bronnen die u normaal gezien zou vertrouwen.
</p>
<h5>
    Hoe verloopt de studie?
</h5>
<p>
    De studie duurt in totaal een maand. We vragen u ook om het volledig proces te doorlopen.
    <br>
    <br>
    Als deelnemer van de studie zal u volgende stappen doorlopen:
    <ol>
        <li>U registreert zich</li>
        <li>Een eerste vragenlijst bepaald uw type persoon (Gamified UK User Type Test)</li>
        <li>Een tweede vragenlijst rond mediagebruik</li>
        <li>Tutorial voor gebruik van het platform</li>
        <li>Start gebruik</li>
        <li>Na twee weken: start gebruik van gepersonaliseerde gamification</li>
        <li>Na vier weken: u krijgt een e-mail of melding om om een SUS-vragenlijst in te vullen en een eindenquête</li>
        <li>(optioneel) Het platform blijven gebruiken met de gepersonaliseerde gamification</li>
    </ol>
    We lichten graag stap 5 beter toe. <b>U leest uw nieuws zoals gewoonlijk</b>, maar nu via ons platform. U krijgt de mogelijkheid om meerdere mediabronnen (de grootse Vlaamse online nieuwsbronnen) aan te duiden, alsook (indien gewenst) meerdere categorieën (binnenland, wetenschap, sport…) aan nieuws. Naast het gewoon lezen, krijgt u de mogelijkheid om <b>aan te geven of u het artikel geloofwaardig vindt</b> of niet. Als er genoeg scores worden verzameld, wordt dit ook getoond naast de titel van een artikel in uw nieuwsfeed. Naast een score te geven, <b>kan u ook opmerkingen plaatsen</b> bij of in de tekst. Hierbij zetten we u aan om bijvoorbeeld bronnen toe te voegen om de claim te bekrachtigen of juist te ontkrachten.
    <br>
    <br>
    Zoals u ziet staan bij stap 6, gaat u na twee weken gepersonaliseerde gamificationtechnieken krijgen. Dit houdt in dat er techniek wordt toegepast dat afhangt van het type persoon dat u bent. Deze gamification dient om u te motiveren om (meer) te gaan fact checken. U kan het zien als een verbetering van de ervaring met het platform.
    <br>
    <br>
    Merk ook op dat indien u een paar dagen niet actief bent, u een e-mail zal ontvangen om uw gebruik verder te zetten. Dit is van belang om goede data te verkrijgen voor het onderzoek.
</p>
<h5>
    Zal mijn deelname aan het onderzoek vertrouwelijk blijven?
</h5>
<p>
    Ja.
    <br>
    <br>
    Een uitgebreider antwoord geeft dat uw gegevens anoniem worden verwerkt. U moet uw voornaam, achternaam, e-mailadres en wachtwoord geven, maar dit dient enkel om u in te loggen. Dit is volledig ontkoppelt van de data die we verzamelen. Alle gegevens worden gelinkt aan een nummer en niet uw naam en uw naam zal nooit vermeld worden in de finale tekst van de masterthesis.
    <br>
    <br>
    De bescherming van de persoonlijke gegevens is wettelijk bepaald door de wet betreffende de bescherming van de persoonlijke levenssfeer (de Belgische wet van 8 december 1992) en GDPR-wetgeving. Als u akkoord gaat om aan de studie deel te nemen, zullen uw persoonlijke gegevens (zoals resultaten van vragenlijsten) voor deze studie steeds worden geanonimiseerd en vertrouwelijk worden behandeld volgens de huidige Belgische wetgeving.
</p>
<h5>
    Ben ik verplicht deel te nemen?
</h5>
<p>
    U beslist vrijwillig of u zal deelnemen of niet. Wanneer u beslist deel te nemen, kan u op elk ogenblik uw toestemming intrekken en hoeft u hiervoor geen reden te geven.
</p>
<h5>
    Wat zijn de mogelijke voor- en nadelen van mijn deelname?
</h5>
<p>
    Uw deelname aan het onderzoek brengt geen enkel fysiek risico met zich mee, maar we zullen u meermaals een <i>fake news</i> artikel aanbieden in uw nieuwsfeed. Hierbij kan u dus <b>gemisinformeerd worden</b>. Indien u twijfel heeft over de echtheid van artikels kan u steeds contact opnemen met de onderzoekers (de contactgegevens zijn onderaan te vinden).
    <br>
    <br>
    Deelname aan de studie zal geen bijkomende kosten met zich mee brengen voor u als deelnemer.
    <br>
    <br>
    We hopen dat na gebruik van ons platform u beter in staat zal zijn om <b>echt en vals nieuws uit elkaar te houden</b>.
</p>
<h5>
    Kan ik weigeren om deel te nemen aan de studie? Kan ik vragen om de studie vroegtijdig te stoppen?
</h5>
<p>
    Uw deelname aan deze studie is vrijwillig. U mag weigeren om deel te nemen of u mag uw deelname aan de studie op ieder ogenblik stopzetten. Als u uw toestemming intrekt, zullen de gegevens bewaard blijven die tot op het ogenblik van uw stopzetting werden verzameld. Dit om de geldigheid van de studie te garanderen. U zal niet geweerd worden voor deelname aan toekomstige studies.
</p>
