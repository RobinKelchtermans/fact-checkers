<h3 class="mt-4">Hoe moet ik fact checken?</h3>
<p>1. <b>Zoek naar wat andere fact-checkers voor je hebben gevonden</b><br />
    We willen allemaal graag origineel zijn, maar het is zeldzaam om de absolute eerste fact checker te zijn die naar een claim kijkt. Vaker wel dan niet, heeft iemand anders onderzocht en geschreven wat je aan het onderzoeken bent, of op zijn minst iets soortgelijks.</p>
    
<p>2. <b>Voer een zoekopdracht uit op Google - en zoek vervolgens opnieuw<br />
</b>Ja, een Google-zoekopdracht is eenvoudig, maar het algoritme van Google is krachtig. Als je een paar woorden begint in te typen, let dan goed op om te zien welke termen door Google worden voorgesteld. Met de geavanceerde zoekinstellingen van Google kan je naar specifieke sites en tijdsperioden kijken.<br />
Neem geen genoegen met het typen van slechts een of twee vragen. Zoek met zoveel mogelijk verschillende combinaties. Als we schrijven over zoiets als klimaatverandering, kunnen we zoeken naar "klimaatverandering", "opwarming van de aarde", "koolstofemissies", "elektriciteit"…<br />
Daag jezelf uit om te leren hoe je de zoekoperatoren van Google kan gebruiken, zodat je kan zoeken op bestandstype (bestand: pdf) of domeinnaam (site: politifact.com). Het kost wat tijd om de geavanceerde mogelijkheden van Google te leren, maar het is de moeite waard.</p>

<p>3. <b>Zoek naar experts met verschillende perspectieven<br />
</b>Experts kunnen je wijzen op onderzoek dat je misschien zelf niet vindt en ze geven vaak belangrijke context aan onderzoek dat je al hebt gevonden. Experts kunnen je vaak behoeden voor verkeerde aannames over gecompliceerde onderwerpen.<br />
Zorg ervoor dat je experts vraagt om je te helpen meer experts te vinden. Grote vragen die moeten worden gesteld, zijn: "Is er iemand wiens mening u respecteert en die het niet met u eens kan zijn?" of: "Is er iemand die u zou aanbevelen die als de autoriteit over dit onderwerp wordt beschouwd?".</p>

<p>4. <b>Boeken &amp; Papers<br />
</b>Zoek ook eens naar boeken. Je hoeft natuurlijk niet het hele boek te lezen, maar je kan er meestal uitleg vinden over gecompliceerde onderwerpen. Hetzelfde geldt voor papers.</p>

<p>5. <b>Neem een pauze en kom terug</b><br />
We raden je aan om na het fact checken even een pauze te nemen, je gedachten te verzetten en nadien terug te komen. Lees na wat je hebt geschreven. Misschien kom je met betere zoektermen, kan je hier en daar aanvullingen geven en vooral, kijk wat de andere mensen ondertussen hebben geschreven.</p>
    