<p>
    De Fake-o-Meter is een schaal met zes verschillende niveaus die het level van juistheid van een artikel aangeeft. 
    Dit wil bijvoorbeeld zeggen dat een artikel dat volledig juist is, objectief geschreven is, een beoordeling “Waar” 
    krijgt en een artikel dat geschreven is om mensen te misleiden, compleet foute informatie bevat een beoordeling “Fake news” krijgt.
</p>

<h3 class="mt-4">Criteria</h3>
<p>
    Om uniformiteit te hebben op het vlak van beoordelingen, stel je je best de volgende vragen af tijdens het fact checken:
    <ul>
        <li>Zijn er bronnen?</li>
        <li>Wie is de auteur? (merk op dat het tijdens dit onderzoek niet mogelijk is om de auteur te zien, maar dat dit wel een <i>good practice</i> is)</li>
        <li>Is dit artikel subjectief of objectief geschreven?</li>
        <li>Komt er een (politieke mening) naar boven?</li>
        <li>Wat is de toon van het artikel?</li>
    </ul>
</p>

