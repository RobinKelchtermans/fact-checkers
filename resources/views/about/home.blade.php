<h3 class="mt-4">Wat is fact checking?</h3>
<p>
    De laatste jaren is er veel te doen rond de term fake news. Op zich bestaat het principe van fake news al veel langer, 
    namelijk mensen proberen te misinfomeren. Met de groeiende communicatiemogelijkheden die het internet ons aanbiedt, 
    is het verspreiden van foutieve informatie makkelijker geworden. Een tegenreactie is het nagaan van artikels, berichten, tweets… op hun juistheid, 
    ook wel <b>fact checking</b> genoemd. Er bestaan meerdere organisatie die dit al aanbieden, zoals FactCheck.org, PolitiFact… 
    Hier gaan professionele fact checkers dagelijks de (sociale) media af om aan fact checking te doen.
</p>
<p>
    Vlachos et al. definieert fact checking als de toekenning van een waarheidswaarde aan een claim in een bepaalde context.
</p>
<blockquote class="blockquote text-right shadow blockquote-border p-2">
    <p class="mb-0">"We define fact checking to be the assignment of a truth value to a claim made in a particular context."</p>
    <footer class="blockquote-footer">Vlachos et al.</footer>
</blockquote>
<p>
    Dit wil zeggen dat je een claim (op dit platform zijn het artikels) gaat nagaan op hun waarheidswaarde. 
    We gebruiken hiervoor de Fake-o-Meter. Hoe faker het artikel, hoe slechter. <a href="#FOM" class="openFOM">Meer informatie over de Fake-o-Meter</a>.
    <br>
    Bij het fact checken vragen we jou om zo veel mogelijk bronnen toe te voegen (als opmerking). Zo weten andere gebruikers waarop je je baseert.
</p>


<h3 class="mt-4">Wat is crowdsourcing?</h3>
<p>
    Een probleem van fact checking is dat het ofwel niet grondig gebeurt, ofwel traag is. 
    Er wordt steeds gestreefd om grondig aan fact checking te doen, dus hierop kan er niet ingeboet worden. 
    Detectie is een vertragende factor en natuurlijk de bronnen die verzameld worden door de fact checkers. 
    Dit onderzoek gaat dat laatste gaan crowdsourcen. Crowdsourcen is het gebruikmaken van een grote groep niet vooraf gespecificeerde individuen 
    (professionals, vrijwilligers, geïnteresseerden) voor consultancy, innovatie, beleidsvorming en onderzoek. Een bekent voorbeeld hiervan is Wikipedia. 
    In dit onderzoek willen we dit toepassen op het Vlaamse nieuws in combinatie met gepersonaliseerde gamification.
</p>


<h3 class="mt-4">Wat is gamification?</h3>
<p>
    Gamification wordt gedefinieerd als het gebruik van game elementen in een non-game context. 
    Een voorbeeld hiervan in een loop-applicatie zou zijn dat een persoon punten krijgt telkens 
    als deze een kilometer heeft gelopen. Aan de hand van die punten kan je je vergelijken met andere lopers. 
    Het geven van punten is een gamificationtechniek, alle gamificationtechnieken samen, worden als gamification gezien. 
    Het doel van deze technieken is om de gebruiker te motiveren om een bepaald doel te bereiken.
</p>
<blockquote class="blockquote text-left shadow blockquote-border p-2">
    <p class="mb-0">"We define “gamification” as the use of game design elements in non-game contexts."</p>
    <footer class="blockquote-footer">Deterding et al.</footer>
</blockquote>
<p>
    Momenteel wordt er vooral onderzoek gedaan naar de effecten van gamification, welke technieken het beste effect hebben e.d. 
    Dit onderzoek is stillaan matuurder aan het worden en wordt er nu onder anderen gekeken naar hoe deze gamificationtechnieken te personaliseren. 
    Dit wil zeggen dat niet elke gebruiker van een systeem dezelfde techniek(en) krijgt.
</p>
