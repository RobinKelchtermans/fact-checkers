<h3>Frequently asked questions</h3>

<p><b>De video’s en afbeeldingen van Twitter/Instagram laden niet goed in.</b></p>
<p>We proberen zo goed mogelijk de artikels op te halen van de verschillende kranten. Echter gebruiken deze soms speciale technologieën, waardoor de opmaak of het laden van video’s niet goed lukt. We proberen dit steeds te verbeteren, maar vergt veel programmeerwerk. Alvast onze excuses!</p>

<p><b>Ik kan de bron zien.</b></p>
<p>Soms is het mogelijk de bron te achterhalen van een artikel. Bijvoorbeeld een video van De Standaard die iets uitlegt. Daar kunnen we moeilijk iets aan doen. We proberen alvast alle artikels zo veel mogelijk “anoniem” te maken.</p>

<p><b>Waarom zijn artikels anoniem?</b></p>
<p>Dit is een onderdeel van het onderzoek. We proberen zo veel mogelijk bias van een artikel te verwijderen, waaronder de bron. Zo proberen we ook na te gaan of gebruikers al dan niet bronnen die ze eigenlijk vertrouwen toch kritisch gaan beoordelen.</p>

